<div class="col-md-7">
     <p>Dr. Knopke completed his undergraduate studies at UCLA with a degree in Biochemistry. He graduated from medical school at the University of Texas, San Antonio Health Science Center. His residency was completed in Family Medicine at Riverside County Regional Medical Center. The last year of his residency he was selected as Chief Resident. He has received extensive training in Obesity Medicine through the <a href="https://www.asbp.org/" title="American Society of Bariatric Physicians" target="_blank">American Society of Bariatric Physicians</a>. The OMA is dedicated to training physicians in the art and science of weight loss and helping patients to achieve optimal health through weight loss.  He is Board Certified in both Obesity Medicine and Family Medicine. He was recently elected to the Board of Trustees for the American Society of Bariatric Physicians.</p>
     <p>After residency, he opened his own practice in association with Raincross Medical Group. He worked part time as a teaching hospitalist at Arrowhead Regional Medical Center. He is a past clinical instructor for first year medical students in the Biomedical Program at The University of California at Riverside. He currently is on Faculty as a Clinical Instructor with the U.C. Riverside Medical School in the department of Family Medicine and teaches both first and second year medical students in his office.</p>
     <p>He is the current chair of the Family Medicine Section at Riverside Community Hospital. His primary medical interests are Obesity Medicine and how this can be applied to treating diabetes, high blood pressure, high cholesterol, and arthritis. He also has special interests in psychiatry and exercise physiology and how they can be applied to treat obesity and associated metabolic conditions.</p>
