        <h3>How We Help You:</h3>
        <p>Our clinic focuses on a multipoint program that addresses all aspects of weight loss.</p>
        <h4>Diet</h4>
        <p>We use a combination of education and medical grade meal replacements to facilitate weight loss.  Each assures an appropriate amount of protein for healthy weight loss and avoidance of hunger.  The education we provide helps you to keep your weight off forever.</p>
        <h4>Appetite Suppressants</h4>
        <p>We provide appetite suppressants to most individuals.  Anorectic medications help you to avoid hunger and increases your metabolic rate to help in weight loss.</p>
        <h4>Medicine</h4>
        <p>Many of our patients also have medical and psychological problems as well.  It is extremely important to optimize the medications that are used for these problems to assure they are not causing a gain or failure to lose weight.</p>
        <h4>Exercise</h4>
        <p>There is no doubt that exercise helps in the weight loss process.  We encourage all of our patients to exercise regularly.  However, we regularly find people who are exercising in the wrong way to promote weight loss.  Often by making subtle changes to your exercise program, we can get you back on the right track to losing weight.</p>
        <h4>Lipotropic Injections</h4>
        <p>Many of our patients will notice a boost to their metabolism and an improvement in their weight loss with a weekly injection.  Each injection contains a blend of methionine, inositol, choline, vitamin B12, L-carnitine, and adenosine.  The combination can help to increase energy levels and encourage fat metabolism.  Ask how you can have this added to your regimen.</p>
        <h4>Support</h4>
        <p>We find that regularly scheduled visits, usually at 2 week intervals, are important in keeping people on track towards their weight loss goal.  We have yet to meet someone who does not stray from the program at some point.  By having you come back regularly, we provide guidance and emotional support to solve problems that come up in many individuals program.</p>
        <h4>Flexible Hours</h4>
        <p>We realize that most of our patients work and scheduling is important for them.  Between our three providers, we have two mornings that start at 7:00 am. If you work locally, then we offer a lunch clinic. Additionally, our clinic has early evening hours until 6:00 pm 2 days per week so that you can come in on your way home.</p>
        <h4>Maintenance</h4>
        <p>When you meet your goal, we tailor the program for you to taper off the frequency of your visits so that we know that you can make it on your own.</p>
        <h4>Price</h4>
